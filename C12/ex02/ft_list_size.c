#include <unistd.h>
#include <stdlib.h>
#include "ft_list.h"

int ft_list_size(t_list *begin_list)
{
    int     count;
    t_list  *ptr;

    count = 0;
    ptr = begin_list;
    while (ptr)
    {
        count++;
        ptr = ptr->next;
    }
    return (count);
}